public class Main {
    public static void main(String[] args) {

        var friends = 19;
        System.out.println("Friends " + friends);
        friends = friends + 2;
        System.out.println("Friends+ " + friends);
        friends = friends / 7;
        System.out.println("Friends- " + friends);

        var frog = 3.5;
        System.out.println("Frog " + frog);
        frog = frog * 10;
        System.out.println("Frog* " + frog);
        frog = frog / 3.5;
        System.out.println("Frog/ " + frog);
        frog = frog + 4;
        System.out.println("Frog+ " + frog);
    }
}